/* Loki:  NOTE:  the SpellClassMask is a 96-bit (12 byte) mask
          almost all class spells have them, they are blank for basically everything else
          the main spells for each class almost always have their own flag(s)
          some minor class spells share flag(s) with other minor class spells
          the flags are NOT unique between classes, only within classes
          eg. the first bit relates to Fireball for Mages but Lightning Bolt for Shamans
          the one exception to this seems to be Gift of the Naaru, which is always bit 95 O.o
          spells with multiple parts sometimes have multiple flags
          the EffectSpellClassMask is set in other spells to tell you which spells that effect affects
          this is mostly set in talents and glyphs, but some class-specific item spells use it too
          flags listed as UNK probably are supposed to do something but I'm not sure what
          flags that are not listed are probably unused and can be used for custom stuff */
///       SOME OF THESE ARE GUESSES... I don't know every single spell, and the DBCs are a mess

